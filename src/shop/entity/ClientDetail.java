package shop.entity;

public class ClientDetail {
	int i;
	String info;
	Client c;
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public Client getC() {
		return c;
	}
	public void setC(Client c) {
		this.c = c;
	}
}
