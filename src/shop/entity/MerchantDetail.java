package shop.entity;

public class MerchantDetail {
	int i;
	String info;
	Merchant m;
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public Merchant getM() {
		return m;
	}
	public void setM(Merchant m) {
		this.m = m;
	}
}
